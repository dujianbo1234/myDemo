function generateXY(n) {
  if (n === 0) return [0, 0];
  //基础增长半径
  let r = 60, degree = 43;
  let x = (r + 12 * n) * Math.sin(n * degree * 2 * Math.PI / 360);
  let y = (r + 12 * n) * Math.cos(n * degree * 2 * Math.PI / 360);
  return [x, y];
}

